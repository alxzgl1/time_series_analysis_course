function p15 = support_wavelet_fft(p11, p12, N)

%% halves into complex form
p13 = N - (length(p11) + length(p12));
p14 = complex(zeros(1, p13), zeros(1, p13));
p15 = [p12, p14, p11];

%% fft
p15 = fft(p15);

end % end

