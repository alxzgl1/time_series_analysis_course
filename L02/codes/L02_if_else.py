def L02_main():

    # if, else
    N = 10
    for n in range(0, N):
      print("n = %d" % (n))
      if n > 7:
        break
      elif n < 3:
        continue
      else:
        m = n * n;
      print("m = %d" % (m))

#------------------------------------------------------------------------------
if __name__ == '__main__':
    L02_main()
